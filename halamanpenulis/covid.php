            Penyebab Virus Corona (COVID-19) <br>
            Penyebab Virus Corona <br>

            Penyebab Corona virus merupakan virus single stranded RNA yang berasal dari kelompok Coronaviridae. Dinamakan coronavirus karena permukaannya yang berbentuk seperti mahkota (crown/corona).

            Virus lain yang termasuk dalam kelompok yang serupa adalah virus yang menyebabkan Middle East Respiratory Syndrome (MERS-CoV) dan Severe Acute Respiratory Syndrome (SARS-CoV) beberapa tahun silam.

            Namun, virus corona dari Wuhan ini merupakan virus baru yang belum pernah teridentifikasi pada manusia sebelumnya. Karena itu, virus ini juga disebut sebagai 2019 Novel Coronavirus atau 2019-nCoV.

            Virus corona umumnya ditemukan pada hewan â€“seperti unta, ular, hewan ternak, kucing, dan kelelawar. Manusia dapat tertular virus apabila terdapat riwayat kontak dengan hewan tersebut, misalnya pada peternak atau pedagang di pasar hewan.

            Namun, adanya ledakan jumlah kasus di Wuhan, China menunjukkan bahwa corona virus dapat ditularkan dari manusia ke manusia. Virus bisa ditularkan lewat droplet, yaitu partikel air yang berukuran sangat kecil dan biasanya keluar saat batuk atau bersin. Apabila droplet tersebut terhirup atau mengenai lapisan kornea mata, seseorang berisiko untuk tertular penyakit ini.

            Meski semua orang dapat terinfeksi virus corona, mereka yang lanjut usia, memiliki penyakit kronis, dan memiliki daya tahan tubuh rendah lebih rentan mengalami infeksi ini serta komplikasinya. <br><br>

            Gejala Virus Corona (COVID-19) <br>
            Demam Tinggi Adalah Gejala Virus Corona <br>

            Gejala Coronavirus bervariasi, mulai dari flu biasa hingga gangguan pernapasan berat menyerupai pneumonia. Gejala Corona yang umum dialami mereka yang mengalami infeksi coronavirus adalah: <br>

            1. Demam tinggi disertai menggigil <br>
            2. Batuk kering <br>
            3. Pilek <br>
            4. Hidung berair dan bersin-bersin <br>
            5. Nyeri tenggorokan <br>
            6. Sesak napas <br> <br>
          
            Gejala virus corona tersebut dapat bertambah parah secara cepat dan menyebabkan gagal napas hingga kematian. Centers for Disease Control and Prevention (CDC) gejala infeksi virus 2019-nCoV dapat muncul mulai dua hari hingga 14 hari setelah terpapar virus tersebut. <br><br>

            Pencegahan Virus Corona (COVID-19) <br>
            Mencuci Tangan Menggunakan Sabun dan Air Mengalir untuk Pencegahan Virus Corona <br>

            Meski gejala penyakit coronavirus menyerupai penyakit pernapasan lain seperti pneumonia atau influenza, sejauh ini belum ada vaksin yang dapat mencegah penularan penyakit coronavirus. Pemberian vaksin pneumonia maupun vaksin influenza tidak dapat memberikan proteksi terhadap penyebaran infeksi virus corona. <br>

            Cara terbaik untuk menghindari penyakit infeksi coronavirus adalah melakukan tindakan pencegahan secara aktif. CDC menyarankan setiap orang melakukan tindakan seperti: <br>

              1. Rutin mencuci tangan menggunakan sabun dan air mengalir selama setidaknya 20 detik <br>
              2. Apabila tidak memungkinkan atau tidak tersedia air dan sabun, bersihkan tangan menggunakan pembersih tangan berbahan alkohol <br>
              3. Hindari menyentuh hidung, mata, atau mulut terutama bila tangan masih kotor <br>
              4. Hindari kontak dengan orang yang sedang sakit <br>
              5. Tetaplah di rumah bila sedang sakit <br>
              6. Tutup mulut dengan tisu atau dengan menekuk siku saat Anda batuk atau bersin <br>
              7. Hindari kontak dengan hewan ternak secara langsung <br>
              8. Hindari bepergian, terutama ke daerah dengan kasus infeksi coronavirus <br>
              9. Hindari mengonsumsi daging yang belum matang sempurna. 
